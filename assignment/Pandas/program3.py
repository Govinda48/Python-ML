# Given the list [1, 2, 3, "Pandas", False, [40, 50, 60]], slice it to get elements from
# the third element to the end of the list. Print the sliced list.

my_list = [1, 2, 3, "Pandas", False, [40, 50, 60]]

# Slice the list from the third element to the end
sliced_list = my_list[2:]

# Print the sliced list
print(sliced_list)